public class switchStatement {
    public static void main(String[] args) {
        int n = 3;
        switch (n) {
            case 1:
                System.out.println("SAMOSA");
                break;
            case 2:
                System.out.println("BURGER");
                break;
            default:
                System.out.println("NOTHING");
                break;
        }
    }
}
